const fs = require("fs");
const path = require("path");

const root = path.resolve(__dirname, "..");
const themesDir = path.join(root, "themes");

function readTheme(file) {
  return JSON.parse(fs.readFileSync(path.join(themesDir, file), "utf8"));
}

function writeTheme(file, theme) {
  fs.writeFileSync(path.join(themesDir, file), `${JSON.stringify(theme, null, 2)}\n`);
}

function applyTokenColors(theme, palette) {
  for (const token of theme.tokenColors) {
    const scopes = Array.isArray(token.scope) ? token.scope : [token.scope];
    const joined = scopes.join(" ");

    if (joined.includes("comment")) {
      token.settings.foreground = palette.comment;
      token.settings.fontStyle = "italic";
    } else if (joined.includes("string") || joined.includes("markup.heading")) {
      token.settings.foreground = palette.string;
    } else if (joined.includes("constant.numeric") || joined.includes("constant.language")) {
      token.settings.foreground = palette.number;
    } else if (joined.includes("keyword") || joined.includes("storage.")) {
      token.settings.foreground = palette.keyword;
    } else if (joined.includes("entity.name.function") || joined.includes("support.function")) {
      token.settings.foreground = palette.function;
    } else if (joined.includes("variable.parameter") || joined.includes("meta.parameter")) {
      token.settings.foreground = palette.parameter;
    } else if (joined.includes("entity.name.type") || joined.includes("entity.name.class")) {
      token.settings.foreground = palette.type;
    } else if (joined.includes("entity.other.attribute-name") || joined.includes("property-name")) {
      token.settings.foreground = palette.property;
    } else if (joined.includes("punctuation") || joined.includes("meta.brace")) {
      token.settings.foreground = palette.punctuation;
    } else if (joined.includes("invalid")) {
      token.settings.foreground = palette.invalidForeground;
      token.settings.background = palette.error;
    } else if (joined.includes("markup.deleted")) {
      token.settings.foreground = palette.error;
    } else if (joined.includes("markup.inserted")) {
      token.settings.foreground = palette.success;
    } else if (joined.includes("markup.changed")) {
      token.settings.foreground = palette.warning;
    } else if (joined.includes("variable")) {
      token.settings.foreground = palette.foreground;
    }
  }
}

function applySemanticColors(theme, palette) {
  theme.semanticTokenColors = {
    class: palette.type,
    enum: palette.type,
    function: palette.function,
    interface: palette.type,
    keyword: palette.keyword,
    method: palette.function,
    namespace: palette.type,
    number: palette.number,
    parameter: palette.parameter,
    property: palette.property,
    string: palette.string,
    type: palette.type,
    variable: palette.foreground
  };
}

